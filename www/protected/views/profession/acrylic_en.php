										<h2>Physical properties of acrylic</h2>
										<p>
											1. The density of acrylic, approx. 1.15-1.19 g/cm3, is lower than that of glass, which is 50% of glass (2.40-2.80 g/cm3) and 43% of metal aluminum (light metal).<br>
											2. Acrylic has a higher mechanical strength. As acrylic is a long-chain polymer compound with a relative molecular mass of approx. 2 million, and the chains that form the molecule are soft, the strength of acrylic is higher, resulting in a stretch and impact resistant capacity 7-18 times high than regular glass. If acrylic undergoes heat and stretch treatment to align the arrangement of its molecular segments in order and significantly increase its toughness, it will not crack even when being penetrated by a nail. As this kind of acrylic will not shatter even when being hit by bullets, the stretch treated acrylic can be used as bullet-proof glass or military aircraft's canopy.<br>
											3. Acrylic has a lower melting point of approx. 130–140°C (265–285°F), which is significantly lower than glass (approx. 1000°C).<br>
											4. Acrylic has a higher transmittance：<br>Visible light: acrylic is the best transparent polymer material up until now with a transmittance of 92%, which is higher than glass.<br>Ultraviolet: while acrylic transmits 73% of UV, quartz can transmit 100% of UV, even though quartz is expensive. Regular glass only transmits 0.6% of UV. Although acrylic can't filter ultraviolet (UV) completely, and UV can penetrate it. A layer of coating can be applied to the surface of acrylic to increase its UV screening effectiveness and property. In addition, compared with polycarbonate, acrylic shows better stability when exposed to UV light. <br>Infrared: acrylic transmits infrared (IR) of a wavelength less than 2800nm, whereas it blocks IR of wavelength less than 25,000nm. There are special color acrylics that allow IR transmission of a specific wavelength and block visible light (applicable to remote control or thermal detection).
